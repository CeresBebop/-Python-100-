# @desc : 存钱问题（通过多重循环穷举20年内收益最大的存款组合）

if __name__ == "__main__":
    
    # x1, x2, x3, x5, x8 分别代表 1年期、2年期、3年期、5年期、8年期的存款次数
    max_amount = 0.0  # 记录最大的本利合计
    
    # 初始化用于记录最终最佳方案的变量
    y1 = y2 = y3 = y5 = y8 = 0 

    # 1. 穷举 8 年期存款的次数 (x8)
    for x8 in range(0, 3):
        
        # 计算在确定了 x8 次数后，剩余年限最多还能存多少次 5 年期
        t5 = (20 - 8 * x8) // 5  
        
        # 2. 穷举 5 年期存款的次数 (x5)
        for x5 in range(0, t5 + 1):
            
            # 计算在确定了 x8 和 x5 后，剩余年限最多还能存多少次 3 年期
            t3 = (20 - 8 * x8 - 5 * x5) // 3  
            
            # 3. 穷举 3 年期存款的次数 (x3)
            for x3 in range(0, t3 + 1):
                
                # 计算在确定了 x8, x5, x3 后，剩余年限最多还能存多少次 2 年期
                t2 = (20 - 8 * x8 - 5 * x5 - 3 * x3) // 2  
                
                # 4. 穷举 2 年期存款的次数 (x2)
                for x2 in range(0, t2 + 1):
                    
                    # 5. 剩余的年限全部自动分配给 1 年期存款 (x1)
                    x1 = 20 - 8 * x8 - 5 * x5 - 3 * x3 - 2 * x2  
                    
                    # 确保 x1 是一个合法次数（大于等于0）
                    if x1 >= 0:
                        
                        # 【已补全】计算当前存款组合下的本利合计 (复利计算)
                        # 各期限月利率：1年=0.0063, 2年=0.0066, 3年=0.0069, 5年=0.0075, 8年=0.0084
                        result = 2000 * ((1 + 1 * 0.0063 * 12) ** x1) * \
                                        ((1 + 2 * 0.0066 * 12) ** x2) * \
                                        ((1 + 3 * 0.0069 * 12) ** x3) * \
                                        ((1 + 5 * 0.0075 * 12) ** x5) * \
                                        ((1 + 8 * 0.0084 * 12) ** x8)
                        
                        # 如果当前组合获取的本利合计比之前记录的都要大，则更新最大值
                        if result > max_amount:
                            max_amount = result  # 记录新的最高本利合计
                            
                            # 记录当前获得最高利息的各个存款年限的次数
                            y1 = x1
                            y2 = x2
                            y3 = x3
                            y5 = x5
                            y8 = x8

    # 输出最终穷举优化后的最佳结果
    print("获得利息最多的存款方式为: ")
    print("8年期限的存了%d次" % y8)
    print("5年期限的存了%d次" % y5)
    print("3年期限的存了%d次" % y3)
    print("2年期限的存了%d次" % y2)
    print("1年期限的存了%d次" % y1)
    print("存款人最终的获得的本利合计: %.2f" % max_amount)